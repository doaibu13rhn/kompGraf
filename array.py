# Membuat List (Array) menggunakan kurung siku []
motor = ["Vario", "Nmax", "Beat", "Aerox"]

# Cara aksesnya pakai Indeks (Ingat: Mulai dari 0!)
print(f"Motor pertama: {motor[0]}") # Output: Vario

# Menambah data ke urutan terakhir
motor.append("PCX")

# Mengubah data
motor[2] = "Scoopy" # Beat diganti jadi Scoopy

print(f"Daftar motor terbaru: {motor}")